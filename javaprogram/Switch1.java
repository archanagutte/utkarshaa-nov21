public class Switch1{
public static void main(String[] args){
String city="pune";

switch (city) {
case "pune":System.out.println("pune");
break;
case "nasik":System.out.println("nasik");
break;
case "parli":System.out.println("parli");
break;
case "beed":System.out.println("beed");
break;
}}

}